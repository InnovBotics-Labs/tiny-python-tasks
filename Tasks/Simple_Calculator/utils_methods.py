"""Contains the UTILS methods"""
# for Add operation
def add(number1, number2):
    """adds 2 elements"""
    return number1 + number2

# for Subtract
def sub(number1, number2):
    """Subtract 2 elements"""
    return number1 - number2


# for Multiply operation
def mul(number1, number2):
    """mul2 elements"""
    return number1 * number2

# for division
def div(number1, number2):
    """div 2 elements"""
    return number1 / number2
